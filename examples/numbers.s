(_inline (.data) (digits: .ascii "0123456789\0") (.text))
(_function pn (n)
	(_if (_or (_equal_to n $0) (_less_than n $0))
		((_identity n))
		((pn (_div n $10))
		 (_print_byte (_add $digits (_mod n $10)) (_deref $STDOUT)))))

(_function _print_number (n)
	(_if (_equal_to n $0)
		((_print_byte $digits (_deref $STDOUT)))
		((pn n))))

(_function _main_ ()
	(_print_number $3469102)
	(_print_newline (_deref $STDOUT))
	(_print_number $45)
	(_print_newline (_deref $STDOUT))
	(_print_number $999999)
	(_print_newline (_deref $STDOUT))
	(_print_number $987654311)
	(_print_newline (_deref $STDOUT))
	(_print_number $1)
	(_print_newline (_deref $STDOUT))
	(_print_number $0)
	(_print_newline (_deref $STDOUT)))
